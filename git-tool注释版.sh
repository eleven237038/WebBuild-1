#!/bin/bash
# ==============================================================================
# Git 快捷工具 - 中文界面
# 文件: git-tool.sh
# 描述: 一个交互式的 Git 常用命令菜单工具,适合 Git 入门学习
# ==============================================================================

# ==============================================================================
# 第一部分: 环境设置
# ==============================================================================

# set -e: 遇到任何命令执行失败(返回非0状态码)时,立即退出脚本
# 作用: 防止脚本在错误命令后继续执行,避免造成更大的问题
# 注意: 在调试时可能需要注释掉这行,以查看所有命令的执行结果
set -e

# ==============================================================================
# 第二部分: 颜色变量定义
# ==============================================================================

# ANSI 转义序列说明:
#   \033 或 \x1b: 转义字符 ESC 的表示方法
#   [数字;数字m: 控制颜色/样式的前缀,0m 表示重置
#
# 格式: \033[属性代码;前景色代码;背景色代码m

# 前景色(文字颜色)代码:
#   30=黑, 31=红, 32=绿, 33=黄, 34=蓝, 35=紫, 36=青, 37=白

# 常用属性代码:
#   0  = 重置/默认
#   1  = 高亮/粗体
#   2  = 低亮
#   3  = 斜体
#   4  = 下划线

RED='\033[0;31m'       # 红色 - 用于错误提示
GREEN='\033[0;32m'     # 绿色 - 用于成功提示
YELLOW='\033[1;33m'    # 黄色(高亮) - 用于警告/进行中提示
BLUE='\033[0;34m'      # 蓝色 - 用于标题/菜单
NC='\033[0m'           # No Color - 重置颜色,恢复到默认颜色
                       # 使用方法: 在彩色文字末尾加上 ${NC} 恢复默认颜色

# ==============================================================================
# 第三部分: 全局变量
# ==============================================================================

# $(command) 是命令替换语法: 执行 command 并捕获其输出
# basename: 提取路径的最后一部分(文件名或目录名)
# pwd: 打印当前工作目录
CURRENT_DIR=$(basename "$(pwd)")

# ==============================================================================
# 第四部分: 函数定义
# 函数是组织代码的基本单元,类似于其他语言的"方法"或"过程"
# 定义格式: function_name() { ... }
# 调用格式: function_name 或者 function_name 参数
# ==============================================================================

# ------------------------------------------------------------------------------
# 显示菜单函数
# ------------------------------------------------------------------------------
show_menu() {
    # clear: 清除终端屏幕
    clear

    # echo -e: 输出字符串,-e 表示启用转义字符解析
    # ${BLUE} ... ${NC}: 彩色输出,BLUE 定义前景色为蓝色

    echo -e "${BLUE}=======================================${NC}"
    echo -e "${BLUE}      Git 快捷工具 - 中文界面${NC}"
    echo -e "${BLUE}=======================================${NC}"
    echo ""
    echo -e "当前目录: ${YELLOW}$(pwd)${NC}"
    echo ""
    # 菜单选项,数字前加颜色便于识别
    echo -e "${GREEN}1${NC}. 初始化仓库/git init"
    echo -e "${GREEN}2${NC}. 查看状态/git status"
    echo -e "${GREEN}3${NC}. 添加并提交/git add + commit"
    echo -e "${GREEN}4${NC}. 查看日志/git log"
    echo -e "${GREEN}5${NC}. 上传代码/git push"
    echo -e "${GREEN}6${NC}. 下载代码/git pull"
    echo -e "${GREEN}7${NC}. 查看差异/git diff"
    echo -e "${GREEN}8${NC}. 查看分支/git branch"
    echo -e "${GREEN}9${NC}. 切换分支/git checkout"
    echo -e "${GREEN}10${NC}. 合并分支/git merge"
    echo -e "${GREEN}0${NC}. 退出"
    echo ""
    echo -e "${BLUE}=======================================${NC}"

    # echo -n: 不换行输出,光标停留在行尾
    echo -n "请输入选项 [0-10]: "
}

# ------------------------------------------------------------------------------
# 初始化仓库函数
# ------------------------------------------------------------------------------
init_repo() {
    echo -e "${YELLOW}正在初始化 Git 仓库...${NC}"
    # git init -b main: 初始化仓库并设置默认分支名为 main
    #   -b: 指定初始分支名称
    #   main: 替代传统的 master 分支名
    git init -b main
    echo -e "${GREEN}✓ 仓库初始化完成！${NC}"
}

# ------------------------------------------------------------------------------
# 查看状态函数
# ------------------------------------------------------------------------------
show_status() {
    echo -e "${YELLOW}正在查看状态...${NC}"
    git status
}

# ------------------------------------------------------------------------------
# 添加并提交函数
# ------------------------------------------------------------------------------
commit_changes() {
    # read -r msg: 读取用户输入,-r 防止反斜杠转义
    # 用户输入存入变量 msg
    echo -n "请输入提交信息: "
    read -r msg

    # [ -z "$msg" ]: 测试字符串是否为空
    #   -z: 字符串长度为0时为真
    #   -n: 字符串长度非0时为真
    if [ -z "$msg" ]; then
        # 如果用户未输入提交信息,使用时间戳作为默认信息
        # date '+%Y-%m-%d %H:%M:%S': 格式化输出当前时间
        #   %Y=年, %m=月, %d=日, %H=时, %M=分, %S=秒
        msg="更新于 $(date '+%Y-%m-%d %H:%M:%S')"
    fi

    echo -e "${YELLOW}正在添加文件并提交...${NC}"
    # git add -A: 添加所有文件到暂存区
    #   -A 或 --all: 包含新文件、修改文件、删除文件
    git add -A
    # git commit -m: 提交暂存区的文件
    #   -m: 指定提交信息
    git commit -m "$msg"
    echo -e "${GREEN}✓ 提交完成！${NC}"
}

# ------------------------------------------------------------------------------
# 查看日志函数
# ------------------------------------------------------------------------------
show_log() {
    echo -e "${YELLOW}最近10条提交记录:${NC}"
    # git log: 查看提交历史
    #   -10: 只显示最近10条
    #   --graph: 显示 ASCII 图形化分支图
    #   --pretty=format: 自定义输出格式
    #     %h = 短哈希值
    #     %d = 分支和标签信息
    #     %s = 提交信息
    #     %cr = 相对时间(如 "2 hours ago")
    #     %an = 作者名
    #   --abbrev-commit: 使用短哈希(7位)
    git log -10 --graph --pretty=format:'%h -%d %s (%cr) <%an>' --abbrev-commit
}

# ------------------------------------------------------------------------------
# 上传代码函数
# ------------------------------------------------------------------------------
push_code() {
    echo -e "${YELLOW}正在上传代码...${NC}"
    git push
    echo -e "${GREEN}✓ 上传完成！${NC}"
}

# ------------------------------------------------------------------------------
# 下载代码函数
# ------------------------------------------------------------------------------
pull_code() {
    echo -e "${YELLOW}正在下载代码...${NC}"
    # git pull: 拉取远程更新并合并
    #   --no-rebase: 即使配置了 rebase,也使用 merge 方式合并
    #   这样可以保持更清晰的提交历史
    git pull --no-rebase
    echo -e "${GREEN}✓ 下载完成！${NC}"
}

# ------------------------------------------------------------------------------
# 查看差异函数
# ------------------------------------------------------------------------------
show_diff() {
    echo -e "${YELLOW}查看文件差异:${NC}"
    # git diff: 显示工作区与暂存区的差异
    #   不加参数: 查看未暂存的修改
    #   --staged: 查看已暂存的修改
    git diff
}

# ------------------------------------------------------------------------------
# 查看分支函数
# ------------------------------------------------------------------------------
show_branch() {
    echo -e "${YELLOW}当前分支:${NC}"
    # git branch: 分支管理
    #   -a 或 --all: 显示所有分支(包括远程)
    git branch -a
}

# ------------------------------------------------------------------------------
# 切换分支函数
# ------------------------------------------------------------------------------
switch_branch() {
    echo -n "请输入分支名: "
    read -r branch

    # 输入验证: 确保分支名不为空
    if [ -z "$branch" ]; then
        echo -e "${RED}分支名不能为空！${NC}"
        # return: 从函数返回,不再执行后续代码
        #   不带参数默认为 return 0(成功)
        return
    fi

    echo -e "${YELLOW}正在切换到分支: $branch${NC}"
    # git checkout: 切换分支或恢复工作区文件
    #   直接跟分支名: 切换到指定分支
    git checkout "$branch"
    echo -e "${GREEN}✓ 切换完成！${NC}"
}

# ------------------------------------------------------------------------------
# 合并分支函数
# ------------------------------------------------------------------------------
merge_branch() {
    echo -n "请输入要合并的分支名: "
    read -r branch

    # 输入验证
    if [ -z "$branch" ]; then
        echo -e "${RED}分支名不能为空！${NC}"
        return
    fi

    echo -e "${YELLOW}正在合并分支: $branch${NC}"
    # git merge: 将指定分支合并到当前分支
    git merge "$branch"
    echo -e "${GREEN}✓ 合并完成！${NC}"
}

# ------------------------------------------------------------------------------
# Git 仓库检查函数
# ------------------------------------------------------------------------------
check_git_repo() {
    # [ ! -d ".git" ]: 测试 .git 目录是否不存在
    #   -d: 测试路径是否为目录且存在
    #   !: 逻辑非运算符
    if [ ! -d ".git" ]; then
        # return 1: 返回失败状态码(非0表示失败)
        return 1
    fi
    # return 0: 返回成功状态码
    return 0
}

# ==============================================================================
# 第五部分: 主程序入口
# ==============================================================================

# while true; do ... done: 无限循环
#   true: 总是返回成功(状态码0)的命令
#   break: 跳出循环
#   continue: 跳过本次迭代,进入下一次

while true; do
    # 显示菜单
    show_menu

    # read -r choice: 读取用户输入并存入 choice 变量
    # -r 参数防止反斜杠被解释为转义字符
    read -r choice

    # case 语句: 多分支选择,类似于其他语言的 switch-case
    # 语法:
    #   case 变量 in
    #     模式1) 命令列表 ;;
    #     模式2) 命令列表 ;;
    #     *) 默认命令列表 ;;  (相当于 default)
    #   esac
    #
    # 模式匹配支持:
    #   *  : 任意字符(相当于通配符)
    #   ?  : 单个任意字符
    #   [abc] : 匹配 a、b 或 c 中的任一字符

    case $choice in
        1)
            init_repo
            ;;
        2)
            show_status
            ;;
        3)
            commit_changes
            ;;
        4)
            show_log
            ;;
        5)
            push_code
            ;;
        6)
            pull_code
            ;;
        7)
            show_diff
            ;;
        8)
            show_branch
            ;;
        9)
            switch_branch
            ;;
        10)
            merge_branch
            ;;
        0)
            # 退出程序
            echo -e "${BLUE}感谢使用 Git 快捷工具！${NC}"
            # exit 0: 退出脚本,0 表示正常退出
            exit 0
            ;;
        *)
            # *) 匹配所有未在上面列出的情况
            # 例如用户输入了菜单中没有的选项
            echo -e "${RED}无效选项，请重新输入！${NC}"
            ;;
    esac

    echo ""
    # 暂停,等待用户按 Enter 键继续
    # 这样用户可以查看命令输出后再继续
    echo -n "按 Enter 键继续..."
    read -r
done

# ==============================================================================
# 附录: 常用 Shell 脚本概念速查
# ==============================================================================
#
# 1. 变量定义与使用
#    定义: var="hello"        (等号两边不能有空格!)
#    使用: echo "$var" 或 echo ${var}
#    只读: readonly var       (定义常量)
#
# 2. 引号区别
#    "双引号": 允许变量替换和转义
#    '单引号': 原样输出,不进行任何替换
#    `反引号`: 等同于 $(...), 用于命令替换
#
# 3. 条件测试 [ ...
#    字符串: [ -z "$str" ] (空) [ -n "$str" ] (非空)
#    数字:    [ $a -eq $b ] (等于) [ $a -ne $b ] (不等于)
#             [ $a -lt $b ] (小于) [ $a -gt $b ] (大于)
#    文件:    [ -f "file" ] (是文件) [ -d "dir" ] (是目录)
#             [ -e "path" ] (存在) [ -r/w/x "path" ] (可读/写/执行)
#
# 4. 逻辑运算符
#    && : 逻辑与 (前一个命令成功才执行下一个)
#    || : 逻辑或 (前一个命令失败才执行下一个)
#    ;  : 命令分隔符 (顺序执行所有命令)
#
# 5. 管道与重定向
#    |  : 将前一个命令的输出作为下一个命令的输入
#    >  : 重定向输出到文件(覆盖)
#    >> : 重定向输出到文件(追加)
#    <  : 从文件读取输入
#    2> : 重定向错误输出
#
# 6. 特殊变量
#    $0 : 脚本名称
#    $1, $2... : 第1、2...个参数
#    $# : 参数个数
#    $@ : 所有参数列表
#    $$ : 当前进程 PID
#    $? : 上一条命令的退出状态(0=成功)
#
